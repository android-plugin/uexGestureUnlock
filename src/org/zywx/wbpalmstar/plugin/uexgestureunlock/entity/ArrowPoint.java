package org.zywx.wbpalmstar.plugin.uexgestureunlock.entity;

public class ArrowPoint {
    private float x;
    private float y;

    public ArrowPoint() {
    }

    public ArrowPoint(float x, float y) {
        this.x = x;
        this.y = y;
    }

    public float getX() {
        return x;
    }

    public void setX(float x) {
        this.x = x;
    }

    public float getY() {
        return y;
    }

    public void setY(float y) {
        this.y = y;
    }
}
